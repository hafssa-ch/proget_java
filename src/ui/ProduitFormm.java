    public void load() {
        model.setRowCount(0);
        for (Produit p : ps.findAll()) {
            model.addRow(new Object[]{
                p.getId(),
                p.getLibelle(),
                p.getCategorie(),
                p.getPrix(),
                p.getStock()
            });
        }
    }


   


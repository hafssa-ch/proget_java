    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
       Produit p = new Produit(
            libelle.getText(),
            Categorie.getText(),
            Float.parseFloat(prix.getText()),
            Integer.parseInt(stock.getText())
        );

        if (ps.create(p)) {
            JOptionPane.showMessageDialog(this, "Produit ajouté !");
            load();
        }
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnmodifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifyActionPerformed
        if (selectedId != -1) {
        Produit p = ps.findById(selectedId);
        p.setLibelle(libelle.getText());
        p.setCategorie(Categorie.getText());
        p.setPrix(Float.parseFloat(prix.getText())); // Float si prix est float
        p.setStock(Integer.parseInt(stock.getText()));

        if(ps.update(p)) {
            JOptionPane.showMessageDialog(this, "Produit modifié !");
            load();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un produit !");
    }
    }//GEN-LAST:event_btnmodifyActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
       if (selectedId != -1) {
        Produit p = ps.findById(selectedId);
        if (ps.delete(p)) {
            JOptionPane.showMessageDialog(this, "Produit supprimé !");
            load();
            selectedId = -1; // réinitialiser la sélection
            libelle.setText("");
            Categorie.setText("");
            prix.setText("");
            stock.setText("");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un produit !");
    }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void listeproduitesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listeproduitesMouseClicked
        int row = listeproduites.getSelectedRow();
    if (row != -1) {
        selectedId = (int) model.getValueAt(row, 0); // récupère l'ID
        libelle.setText(model.getValueAt(row, 1).toString());
        Categorie.setText(model.getValueAt(row, 2).toString());
        prix.setText(model.getValueAt(row, 3).toString());
        stock.setText(model.getValueAt(row, 4).toString());
    }
    }//GEN-LAST:event_listeproduitesMouseClicked



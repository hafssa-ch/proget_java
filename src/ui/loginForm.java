    private void login() {
        String email = utilisateur.getText();
        String password = new String(motdepasse.getPassword());

        if (email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez saisir l'email et le mot de passe !");
            return;
        }

        LoginService ls = new LoginService();
        if (ls.checkLogin(email, password)) {
            JOptionPane.showMessageDialog(this, "Connexion réussie !");
            this.dispose(); // ferme le JInternalFrame
        } else {
            JOptionPane.showMessageDialog(this, "Email ou mot de passe incorrect !");
        }
    }



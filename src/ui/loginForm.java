    private void connecterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connecterActionPerformed
        
   String identifier = utilisateur.getText().trim(); // Peut être username ou email
    String password = new String(motdepasse.getPassword()).trim();

    if (identifier.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Veuillez saisir le nom d'utilisateur/email et le mot de passe !",
            "Champs vides",
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    LoginService ls = new LoginService();
    if (ls.checkLogin(identifier, password)) {
        JOptionPane.showMessageDialog(this, 
            " Connexion réussie !",
            "Succès",
            JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
        
    } else {
        JOptionPane.showMessageDialog(this, 
            " Identifiant ou mot de passe incorrect !\n" +
            "Utilisateur : admin\n" +
            "Mot de passe : admin",
            "Échec de connexion",
            JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_connecterActionPerformed

    private void motdepasseoublierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_motdepasseoublierActionPerformed
       String email = JOptionPane.showInputDialog(this, 
        "Veuillez saisir votre email de récupération :", 
        "Mot de passe oublié", 
        JOptionPane.QUESTION_MESSAGE);
    
    if (email == null || email.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Email non saisi. Opération annulée.", 
            "Annulé", 
            JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    email = email.trim();
    
    if (!email.contains("@") || !email.contains(".")) {
        JOptionPane.showMessageDialog(this, 
            "Format d'email invalide !", 
            "Erreur", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
        "Êtes-vous sûr de vouloir réinitialiser le mot de passe pour :\n" + email + " ?",
        "Confirmation",
        JOptionPane.YES_NO_OPTION);
    
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    LoginService ls = new LoginService();
    if (!ls.checkUserExists(email)) {
        JOptionPane.showMessageDialog(this, 
            "Email introuvable dans la base de données !\n" +
            "Vérifiez l'email saisi.", 
            "Erreur", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    ls.resetPassword(email);
    }//GEN-LAST:event_motdepasseoublierActionPerformed



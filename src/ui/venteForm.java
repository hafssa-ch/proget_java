    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
         try {
            Produit p = (Produit) Produit.getSelectedItem();
            Client c = (Client) Client.getSelectedItem();
            Date date = dateVente.getDate();
            int q = Integer.parseInt(quantite.getText());
            int prix = Integer.parseInt(prixUnitaire.getText());
            Vente v = new Vente(p, c, date, q, prix);
            if (venteService.create(v)) {
                JOptionPane.showMessageDialog(this, "Vente ajoutée !");
                loadVentes();
                clearForm();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erreur : " + ex.getMessage());
        }
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnmodifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifyActionPerformed
        // TODO add your handling code here:
        int row = listeventes.getSelectedRow();
        if (row != -1) {
            try {
                int id = (int) model.getValueAt(row, 0);
                Produit p = (Produit) Produit.getSelectedItem();
                Client c = (Client) Client.getSelectedItem();
                Date date = dateVente.getDate();
                int q = Integer.parseInt(quantite.getText());
                int prix = Integer.parseInt(prixUnitaire.getText());
                Vente v = new Vente(id, p, c, date, q, prix);
                if (venteService.update(v)) {
                    JOptionPane.showMessageDialog(this, "Vente modifiée !");
                    loadVentes();
                    clearForm();
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erreur : " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnmodifyActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
       int row = listeventes.getSelectedRow();
        if (row != -1) {
            int id = (int) model.getValueAt(row, 0);
            Vente v = new Vente();
            v.setId(id);
            if (venteService.delete(v)) {
                JOptionPane.showMessageDialog(this, "Vente supprimée !");
                loadVentes();
                clearForm();
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed


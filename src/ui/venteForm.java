    private void loadClients() {
        Client.removeAllItems();
        for (Client c : clientService.findAll()) {
            Client.addItem(c);
        }
    }

    private void loadProduits() {
        Produit.removeAllItems();
        for (Produit p : produitService.findAll()) {
            Produit.addItem(p);
        }
    }

    private void loadVentes() {
        model = (DefaultTableModel) listeventes.getModel();
        model.setRowCount(0);
        List<Vente> ventes = venteService.findAll();
        for (Vente v : ventes) {
            model.addRow(new Object[]{
                    v.getId(),
                    v.getProduit(),
                    v.getClient(),
                    v.getDate_venete(),
                    v.getQuantite(),
                    v.getPrix_unitaire()
            });
        }
    }


